#include "libft.h"

int	ft_sqrt(int n)
{
	int i;

	i = 0;
	if (n < 0)
		return (0);
	while (i * i != n)
	{
		if (i * i > n)
			return (0);
		if (i * i < n)
			i++;
	}
	return (i);
}
